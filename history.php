<p>School Survey
The annual NT School Survey is on again from Monday 14 August 2017 to Friday 1 September 2017.

The School Survey collects the opinions of staff, students and their families about school performance, culture and services.

It includes a number of national questions that all schools are required to use in their surveys as well as a number of questions tailored to the individual context of each school.

Feedback will be shared with school communities so they can gain a deeper understanding of their school, what it does well and where it can improve.

Who can participate?
The School Survey is open for school staff, students and families to complete.

How can I complete the survey?
As each school’s survey is different, your school will send out a special link to staff, students and families so that you can complete the survey.

School Survey is an easy to complete online survey that takes about 15 – 20 minutes. 

Printed copies of the survey are available for teachers, students and families that don’t have access to online facilities. Schools also often provide access to computers and iPad’s to assist the school community complete their survey.

All individual responses are kept confidential and strict rules are in place to safeguard this at every stage of the survey process.

Results
Schools will provide feedback to their school communities upon completion of the survey.</p>
